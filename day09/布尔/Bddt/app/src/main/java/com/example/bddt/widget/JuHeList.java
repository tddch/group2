package com.example.bddt.widget;

public class JuHeList {
    private Double lot;
    private Double gt;

    public JuHeList(Double lot, Double gt) {
        this.lot = lot;
        this.gt = gt;
    }

    public Double getLot() {
        return lot;
    }

    public void setLot(Double lot) {
        this.lot = lot;
    }

    public Double getGt() {
        return gt;
    }

    public void setGt(Double gt) {
        this.gt = gt;
    }
}
